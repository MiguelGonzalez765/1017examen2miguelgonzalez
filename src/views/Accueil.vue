    <p>Le concept «Nouvelles technologies de l’information et de la communication» (NTIC), est souvent utilisé et compris comme synonyme d’Internet avec tout ce que cela suppose: l’utilisation d’ordinateurs et de divers réseaux de télécommunications permettant de relier les utilisateurs entre eux, et de les relier à l’information.

